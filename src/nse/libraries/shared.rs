pub fn setup_nse_globals(lua: &Lua) -> LuaResult<()> {
    let globals = lua.globals();

    for name in NSE_MODULES {
        globals.set(*name, lua.create_table()?)?;
    }

    globals.set("_REQUIRE_MODULES", lua.create_table()?)?;
    globals.set("_SCRIPT_OUTPUT", lua.create_table()?)?;

    Ok(())
}

pub fn sync_require_modules(lua: &Lua) -> LuaResult<()> {
    let globals = lua.globals();
    let modules = globals.get::<Table>("_REQUIRE_MODULES")?;

    for name in STANDARD_SYNC_MODULES {
        if let Ok(table) = globals.get::<Table>(name) {
            let _ = modules.set(*name, table);
        }
    }

    Ok(())
}
